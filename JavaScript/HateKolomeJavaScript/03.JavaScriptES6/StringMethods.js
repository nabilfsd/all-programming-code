// in es6 there are some useful string methods
// {startsWith, endsWith, inclues} return boolean, repeat
// startsWith
let person = "Md. Mehedi Hasan Nabil"
console.log(person.startsWith('Nabil')); // output: false

// endsWith
console.log(person.endsWith('Nabil')); // output: true

// includes
console.log(person.includes('Hasan')); // output: true

// repeat
console.log(person.repeat(3)); // output: print name 3 times